class user:

    def __init__(self, score, moves):
        self.score = score
        self.moves = moves
        self.won = False
        self.gameOver = False

def isGameOver(self, winningScore):
    
    if self.score >= winningScore:
        self.won = True
        self.gameOver = True


    elif self.moves <= 0 :
        self.gameOver = True
        
        
    

    